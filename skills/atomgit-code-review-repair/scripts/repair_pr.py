#!/usr/bin/env python3
"""
AtomGit PR Review Comment Repair Script
支持三种模式：
1. --fetch-comments: 获取检视意见（输出JSON）
2. --apply-fixes: 应用修复方案（从JSON读取）
3. --auto: 自动修复（调用LLM）
"""

import os
import sys
import json
import argparse
import subprocess
from pathlib import Path
from typing import List, Dict, Optional
from datetime import datetime

from atomgit_sdk import AtomGitClient, AtomGitConfig
from atomgit_sdk.services import RepairService


def load_config(config_path: str) -> dict:
    """加载配置"""
    with open(config_path, "r", encoding="utf-8") as f:
        config = json.load(f)

    if not config.get("atomgit") or not config["atomgit"].get("token"):
        raise Exception("配置文件中缺少 atomgit.token")

    return config


def format_fix_reply(fix: dict, ai_model: str) -> str:
    """格式化修复回复"""
    return f"""感谢您的审阅意见。已按要求完成修复，具体内容如下：

**修复方案**:
{fix.get("fix_description", "")}

**修改文件**: `{fix.get("file_path", "unknown")}`

**代码变更**:
```
# 修改前 (Before)
{fix.get("original_code", "")}

# 修改后 (After)
{fix.get("fixed_code", "")}
```

如有任何疑问，请随时提出。

---
🤖 generated by ai@{ai_model}"""


def format_logic_reply(fix: dict, ai_model: str) -> str:
    """格式化逻辑回复"""
    return f"""感谢您的审阅意见。

{fix.get("reply", "")}

如有任何疑问，请随时提出。

---
🤖 generated by ai@{ai_model}"""


def format_revert_reply(fix: dict, ai_model: str) -> str:
    """格式化回退回复"""
    return f"""感谢您的审阅意见。

**处理结果**:
{fix.get("fix_description", "")}

已按要求回退该文件的修改，恢复到原仓库状态。

如有任何疑问，请随时提出。

---
🤖 generated by ai@{ai_model}"""


def format_delete_lines_reply(fix: dict, ai_model: str) -> str:
    """格式化删除行回复"""
    lines_str = ", ".join(map(str, fix.get("delete_lines", [])))
    return f"""感谢您的审阅意见。

**处理结果**:
{fix.get("fix_description", "")}

已删除以下行号的内容: {lines_str}

如有任何疑问，请随时提出。

---
🤖 generated by ai@{ai_model}"""


def apply_code_fix(file_path: str, original_code: str, fixed_code: str) -> None:
    """应用代码修复"""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    new_content = content.replace(original_code, fixed_code)

    if new_content == content:
        raise Exception("无法找到原始代码。代码可能已更改。")

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"  ✓ 已应用修复到 {file_path}")


def delete_specific_lines(file_path: str, line_numbers: List[int]) -> None:
    """删除特定行"""
    with open(file_path, "r", encoding="utf-8") as f:
        content = f.read()

    lines = content.split("\n")
    line_numbers_sorted = sorted(line_numbers, reverse=True)

    for line_num in line_numbers_sorted:
        if 1 <= line_num <= len(lines):
            del lines[line_num - 1]

    new_content = "\n".join(lines)

    with open(file_path, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"  ✓ 已删除 {file_path} 中的 {len(line_numbers)} 行")


def revert_file(file_path: str) -> None:
    """回退整个文件"""
    result = subprocess.run(
        ["git", "checkout", "HEAD", "--", file_path], capture_output=True, text=True
    )

    if result.returncode != 0:
        raise Exception(f"回退文件失败: {result.stderr}")

    print(f"  ✓ 已回退文件 {file_path}")


def commit_fixes(work_dir: str, ai_model: str) -> None:
    """提交修复"""
    commit_message = f"fix: resolve review comments\n\nGenerated by ai@{ai_model}"

    result = subprocess.run(
        ["git", "add", "."], cwd=work_dir, capture_output=True, text=True
    )

    if result.returncode != 0:
        raise Exception(f"Git add 失败: {result.stderr}")

    result = subprocess.run(
        ["git", "commit", "--amend", "-m", commit_message],
        cwd=work_dir,
        capture_output=True,
        text=True,
    )

    if result.returncode != 0:
        raise Exception(f"Git commit 失败: {result.stderr}")

    print(f"  ✓ 已提交修复 (git commit --amend)")


def mode_fetch_comments(args, service: RepairService):
    """模式1: 获取检视意见"""
    print("\n" + "=" * 60)
    print("📥 模式: 获取检视意见")
    print("=" * 60)

    comments = service.get_unresolved_comments(args.pr)

    if not comments:
        print("\n✅ 没有需要修复的检视意见")
        return

    print(f"\n找到 {len(comments)} 条未解决检视意见")

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    repo_name = service.client.config.repo.lower().replace("-", "_")
    output_file = output_dir / f"{repo_name}_pr_{args.pr}_comments.json"

    output_data = {
        "pr_number": args.pr,
        "fetch_time": datetime.now().isoformat(),
        "comments": comments,
    }

    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(output_data, f, indent=2, ensure_ascii=False)

    print(f"\n✅ 已保存到: {output_file}")
    print("\n💡 下一步:")
    print("  AI Agent 应该:")
    print("  1. 读取此文件并分析检视意见")
    print("  2. 生成修复方案 (fixes.json)")
    print("  3. 将修复方案以用户可读的格式展示给用户确认")
    print("  4. 用户确认后，运行提交命令")
    print(
        f"\n     python3 repair_pr.py --pr {args.pr} --apply-fixes fixes.json --ai-model <your-model-name>"
    )


def mode_apply_fixes(args, service: RepairService):
    """模式2: 应用修复方案"""
    print("\n" + "=" * 60)
    print("🔧 模式: 应用修复方案")
    print("=" * 60)

    with open(args.apply_fixes, "r", encoding="utf-8") as f:
        fixes = json.load(f)

    print(f"\n加载了 {len(fixes)} 个修复方案")

    results = []
    files_fixed = []

    for i, fix in enumerate(fixes, 1):
        print(f"\n[{i}/{len(fixes)}] 处理 {fix.get('file_path', 'N/A')}")

        try:
            fix_type = fix.get("type", "code_fix")
            reply = ""

            if fix_type == "code_fix":
                if args.dry_run:
                    print(f"  [Dry Run] 将修复: {fix['file_path']}")
                    reply = format_fix_reply(fix, args.ai_model)
                else:
                    apply_code_fix(
                        fix["file_path"], fix["original_code"], fix["fixed_code"]
                    )
                    files_fixed.append(fix["file_path"])
                    reply = format_fix_reply(fix, args.ai_model)

            elif fix_type == "delete_lines":
                if args.dry_run:
                    print(f"  [Dry Run] 将删除行: {fix['file_path']}")
                    reply = format_delete_lines_reply(fix, args.ai_model)
                else:
                    delete_specific_lines(fix["file_path"], fix["delete_lines"])
                    files_fixed.append(fix["file_path"])
                    reply = format_delete_lines_reply(fix, args.ai_model)

            elif fix_type == "revert_file":
                if args.dry_run:
                    print(f"  [Dry Run] 将回退: {fix['file_path']}")
                    reply = format_revert_reply(fix, args.ai_model)
                else:
                    revert_file(fix["file_path"])
                    files_fixed.append(fix["file_path"])
                    reply = format_revert_reply(fix, args.ai_model)

            elif fix_type == "reply_only":
                reply = format_logic_reply(fix, args.ai_model)

            else:
                print(f"  ⚠️ 跳过未知的修复类型: {fix_type}")
                results.append(
                    {
                        "file": fix.get("file_path", "N/A"),
                        "comment_id": fix.get("comment_id"),
                        "has_fix": False,
                        "error": f"未知的修复类型: {fix_type}",
                    }
                )
                continue

            if not args.dry_run and reply:
                comment_id = fix.get("comment_id")
                if comment_id:
                    service.reply_to_comment(args.pr, comment_id, reply)
                    print(f"  ✓ 已回复评论 #{comment_id}")

            results.append(
                {
                    "file": fix.get("file_path", "N/A"),
                    "comment_id": fix.get("comment_id"),
                    "has_fix": True,
                    "error": None,
                }
            )

        except Exception as e:
            print(f"  ✗ 处理失败: {e}")
            results.append(
                {
                    "file": fix.get("file_path", "N/A"),
                    "comment_id": fix.get("comment_id"),
                    "has_fix": False,
                    "error": str(e),
                }
            )

    if files_fixed and not args.dry_run:
        print("\n📦 提交修复...")
        commit_fixes(args.work_dir, args.ai_model)

    print("\n" + "=" * 60)
    print("✅ 任务完成!")
    print("=" * 60)

    print(f"\n📊 统计:")
    success_count = sum(1 for r in results if r["has_fix"])
    print(f"  成功: {success_count}/{len(results)}")
    print(f"  失败: {len(results) - success_count}/{len(results)}")
    print(f"\n🔗 PR 链接: {service.get_pr_url(args.pr)}")


def mode_auto(args, service: RepairService, config: dict):
    """模式3: 自动修复（调用LLM）"""
    print("\n" + "=" * 60)
    print("🤖 模式: 自动修复（LLM驱动）")
    print("=" * 60)

    if not config.get("anthropic", {}).get("apiKey"):
        print("\n❌ 自动模式需要配置 Anthropic API Key")
        print("   请在 config.json 中添加:")
        print('   {"anthropic": {"apiKey": "sk-ant-..."}}')
        print("\n或者使用手动模式:")
        print(f"   python3 repair_pr.py --pr {args.pr} --fetch-comments")
        return

    print("\n📝 获取未解决检视意见...")
    comments = service.get_unresolved_comments(args.pr)

    if not comments:
        print("\n✅ 没有需要修复的检视意见")
        return

    print(f"\n找到 {len(comments)} 条未解决检视意见")
    print("\n⚠️ 自动修复模式需要 LLM 集成，请使用手动模式（AI Agent 驱动）:")
    print(f"   python3 repair_pr.py --pr {args.pr} --fetch-comments")


def main():
    parser = argparse.ArgumentParser(
        description="AtomGit PR 检视意见修复工具",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )

    parser.add_argument("--pr", type=int, required=True, help="PR 编号")

    mode_group = parser.add_mutually_exclusive_group(required=False)
    mode_group.add_argument(
        "--fetch-comments", action="store_true", help="模式1: 获取检视意见（输出JSON）"
    )
    mode_group.add_argument(
        "--apply-fixes",
        type=str,
        metavar="JSON_FILE",
        help="模式2: 应用修复方案（从JSON读取）",
    )
    mode_group.add_argument(
        "--auto", action="store_true", help="模式3: 自动修复（调用LLM）"
    )

    parser.add_argument(
        "--config", type=str, default="config.json", help="配置文件路径"
    )
    parser.add_argument("--work-dir", type=str, default=".", help="工作目录")
    parser.add_argument("--output-dir", type=str, default="./tmp", help="输出目录")
    parser.add_argument(
        "--ai-model", type=str, default="ai", help="AI模型名称，用于签名"
    )
    parser.add_argument(
        "--llm-provider", type=str, default="anthropic", help="LLM 提供商"
    )
    parser.add_argument(
        "--llm-model", type=str, default="claude-sonnet-4-20250514", help="LLM 模型名称"
    )
    parser.add_argument("--dry-run", action="store_true", help="仅显示计划")

    args = parser.parse_args()

    if args.ai_model == "ai":
        print("\n⚠️  警告: 未指定 --ai-model 参数，将使用默认值 'ai'")
        print("   建议指定真实模型名称，例如：--ai-model claude-sonnet-4")
        print()

    if not any([args.fetch_comments, args.apply_fixes, args.auto]):
        args.fetch_comments = True
        print("ℹ️  未指定模式，使用默认的提取信息模式（AI Agent 使用）")

    print("\n" + "=" * 60)
    print("🔧 AtomGit PR 检视意见修复工具")
    print("=" * 60)

    try:
        config = load_config(args.config)
    except FileNotFoundError:
        print(f"\n❌ 配置文件不存在: {args.config}")
        sys.exit(1)
    except Exception as e:
        print(f"\n❌ 加载配置失败: {e}")
        sys.exit(1)

    client = AtomGitClient(AtomGitConfig.from_json(args.config))
    service = RepairService(client)

    print(f"\n📋 PR: #{args.pr}")
    print(f"🏠 仓库: {client.config.owner}/{client.config.repo}")
    print(f"🤖 AI模型: {args.ai_model}")

    if args.auto:
        print(f"🧠 LLM模型: {args.llm_model} (provider: {args.llm_provider})")

    if args.dry_run:
        print("⚠️  Dry Run 模式（仅显示计划）")

    if args.fetch_comments:
        mode_fetch_comments(args, service)
    elif args.apply_fixes:
        mode_apply_fixes(args, service)
    elif args.auto:
        mode_auto(args, service, config)


if __name__ == "__main__":
    main()
