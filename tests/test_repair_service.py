#!/usr/bin/env python3
"""
Test RepairService reply_to_comment functionality
"""

from unittest.mock import Mock

import pytest
from atomgit_sdk.services import RepairService


class TestRepairService:
    """Test suite for RepairService"""

    def setup_method(self):
        """Setup test fixtures"""
        self.mock_client = Mock()
        self.mock_client.config = Mock()
        self.mock_client.config.owner = "test_owner"
        self.mock_client.config.repo = "test_repo"
        self.mock_client.get_pr_url.return_value = "https://atomgit.com/openeuler/IB_Robot/pull/84"
        self.service = RepairService(self.mock_client)

    def test_reply_to_comment_threaded_uses_discussion_id(self):
        """Test threaded reply with discussion_id."""
        original_comment = {"id": 123, "discussion_id": "abc456", "body": "Original comment"}
        self.mock_client.get_pr_comment.return_value = original_comment
        expected_response = {"id": 456, "body": "Reply"}
        self.mock_client.reply_to_pr_discussion.return_value = expected_response

        result = self.service.reply_to_comment(1, 123, "Test reply", reply_mode="threaded")
        assert result == expected_response

        self.mock_client.reply_to_pr_discussion.assert_called_once()
        args, _kwargs = self.mock_client.reply_to_pr_discussion.call_args
        assert args[0] == 1
        assert args[1] == "abc456"
        assert "Test reply" in args[2]

    def test_reply_to_comment_visible_posts_pr_comment(self):
        """Test visible reply falls back to a PR-level comment."""
        original_comment = {"id": 124, "discussion_id": "disc-1", "body": "Original comment"}
        self.mock_client.get_pr_comment.return_value = original_comment
        self.mock_client.submit_pr_comment.return_value = {"id": 789}

        result = self.service.reply_to_comment(1, 124, "Test reply", reply_mode="visible")

        assert result["id"] == 789
        assert result["reply_mode"] == "visible"
        assert result["thread_url"].endswith("#discussion-disc-1")
        self.mock_client.submit_pr_comment.assert_called_once()
        args, _kwargs = self.mock_client.submit_pr_comment.call_args
        assert args[0] == 1
        assert "Reply to review comment #124" in args[1]
        assert "Original thread:" in args[1]
        assert "Test reply" in args[1]

    def test_reply_to_diff_comment_visible_posts_inline_comment(self):
        original_comment = {
            "id": 125,
            "discussion_id": "disc-2",
            "body": "Original diff comment",
            "position": {
                "new_path": "src/example.py",
                "new_line": 42,
                "head_sha": "deadbeef",
            },
        }
        self.mock_client.get_pr_comment.return_value = original_comment
        self.mock_client.submit_inline_comment.return_value = {"id": 790}

        result = self.service.reply_to_comment(1, 125, "Test reply", reply_mode="visible")

        assert result["id"] == 790
        assert result["reply_mode"] == "visible"
        self.mock_client.submit_inline_comment.assert_called_once_with(
            1,
            {
                "path": "src/example.py",
                "line": 42,
                "commit_id": "deadbeef",
                "body": (
                    "Reply to review comment #125\n"
                    "Original thread: https://atomgit.com/openeuler/IB_Robot/pull/84#discussion-disc-2\n\n"
                    "Test reply\n\n---\n🤖 Generated by AtomGit SDK"
                ),
            },
        )

    def test_reply_to_nonexistent_comment(self):
        """Test reply to non-existent comment"""
        self.mock_client.get_pr_comment.return_value = None

        with pytest.raises(ValueError, match="Comment 999 not found"):
            self.service.reply_to_comment(1, 999, "Test reply")

    def test_resolve_comment_uses_discussion_id(self):
        self.mock_client.get_pr_comment.return_value = {
            "id": 123,
            "discussion_id": "abc456",
        }
        self.mock_client.set_pr_discussion_resolved.return_value = {"resolved": True}

        result = self.service.resolve_comment(1, 123, True)

        assert result == {"resolved": True}
        self.mock_client.set_pr_discussion_resolved.assert_called_once_with(1, "abc456", True)

    def test_get_review_threads_groups_discussions(self):
        self.mock_client.get_all_pr_comments.return_value = [
            {
                "id": 101,
                "discussion_id": "disc-1",
                "body": "Please fix this",
                "comment_type": "diff_comment",
                "created_at": "2026-05-06T10:00:00+08:00",
                "user": {"login": "reviewer"},
            },
            {
                "id": 102,
                "discussion_id": "disc-1",
                "body": "Done",
                "comment_type": "diff_comment",
                "created_at": "2026-05-06T10:01:00+08:00",
                "user": {"login": "author"},
                "is_outdated": True,
            },
            {
                "id": 201,
                "discussion_id": "disc-2",
                "body": "CI status",
                "comment_type": "pr_comment",
                "created_at": "2026-05-06T10:02:00+08:00",
                "user": {"login": "ci-bot"},
            },
        ]
        self.mock_client.get_current_user.return_value = {"login": "author"}

        result = self.service.get_review_threads(84)

        assert len(result) == 1
        thread = result[0]
        assert thread["id"] == 101
        assert thread["discussion_id"] == "disc-1"
        assert thread["reply_count"] == 1
        assert thread["participants"] == ["author", "reviewer"]
        assert thread["thread_comments"][1]["is_self"] is True
        assert thread["thread_comments"][1]["is_outdated"] is True

    def test_get_review_threads_returns_empty_for_empty_comments(self):
        self.mock_client.get_all_pr_comments.return_value = []
        self.mock_client.get_current_user.return_value = {"login": "author"}

        assert self.service.get_review_threads(84) == []

    def test_get_review_threads_include_self_comments(self):
        self.mock_client.get_all_pr_comments.return_value = [
            {
                "id": 101,
                "discussion_id": "disc-1",
                "body": "Done",
                "comment_type": "diff_comment",
                "created_at": "2026-05-06T10:01:00+08:00",
                "user": {"login": "author"},
            }
        ]
        self.mock_client.get_current_user.return_value = {"login": "author"}

        result = self.service.get_review_threads(84, include_self_comments=True)

        assert len(result) == 1
        assert result[0]["user"] == "author"

    def test_get_review_threads_include_bot_comments(self):
        self.mock_client.get_all_pr_comments.return_value = [
            {
                "id": 201,
                "discussion_id": "disc-2",
                "body": "CI status",
                "comment_type": "pr_comment",
                "created_at": "2026-05-06T10:02:00+08:00",
                "user": {"login": "ci-bot"},
            }
        ]
        self.mock_client.get_current_user.return_value = {"login": "author"}

        result = self.service.get_review_threads(84, include_bot_comments=True)

        assert len(result) == 1
        assert result[0]["user"] == "ci-bot"

    def test_get_unresolved_comments_warns_and_delegates(self):
        self.service.get_review_threads = Mock(return_value=[{"id": 101}])

        with pytest.warns(
            DeprecationWarning,
            match="get_unresolved_comments is deprecated; use get_review_threads instead",
        ):
            result = self.service.get_unresolved_comments(84)

        assert result == [{"id": 101}]
        self.service.get_review_threads.assert_called_once_with(84)

    def test_reply_to_outdated_comment_returns_visibility_hint(self):
        self.mock_client.get_pr_comment.return_value = {
            "id": 123,
            "discussion_id": "disc-1",
            "body": "Original comment",
            "is_outdated": True,
        }
        self.mock_client.reply_to_pr_discussion.return_value = {"id": 456}

        result = self.service.reply_to_comment(1, 123, "Test reply", reply_mode="threaded")

        assert result["id"] == 456
        assert "visibility_hint" in result
        assert result["thread_url"].endswith("#discussion-disc-1")


if __name__ == "__main__":
    pytest.main([__file__, "-v"])
